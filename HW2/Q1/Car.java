public class Car {
    String Name;
    String Brand;
    String Code;
    String Color;
    String Origin;
    double Price;
    double Speed;
    double Strength;
    Car(String Name,String  Brand,String Code){
        this.Name = Name;
        this.Brand = Brand;
        this.Code = Code;
        this.Color = "";
        this.Origin = "";
        this.Price = -1;
        this.Speed = -1;
        this.Strength = -1;
    }
}
